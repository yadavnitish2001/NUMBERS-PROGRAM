package squareroot;
//class method
public class SquareRootApp {
	//method name (non-static method)
			public static void squareroot(double n) {
				//taking result variable and declare result as double
				double result;
				//taking math.sqrt() function for find square root and then store in variable results
				result = Math.sqrt(n);
				//print the results
		        System.out.println("The square Root of number is:"+result);
			}
}
